	@Override
	public boolean onCommand(CommandSender sender, Command command, String label, String[] args) {
		if (args.length >= 1) {
			if (args[0].equalsIgnoreCase("list")){
				if (args.length >= 2) {
					if(args[1].equalsIgnoreCase("enabled")){
				         for (String twist : config.getConfigurationSection("twists").getKeys(false)) {
				             String value = config.getString("twists." + twist);
				             if (value == "true") sender.sendMessage("- " + twist);
				          }
					}
					else if(args[1].equalsIgnoreCase("disabled")){
				         for (String twist : config.getConfigurationSection("twists").getKeys(false)) {
				             String value = config.getString("twists." + twist);
				             if (value == "false") sender.sendMessage("- " + twist);
				          }
					}
				}
				else {
					for (String thing : config.getStringList("liste-twists")) {
						sender.sendMessage("- " + thing);
					}
				}
			}
			if (args[0].equalsIgnoreCase("fixhealth")){
				for(Player player: Bukkit.getOnlinePlayers()) {
					player.setHealth(1);
					player.setMaxHealth(20);
					player.setHealth(20);
					player.setHealthScale(20);
				}
			}
			if (args[0].equalsIgnoreCase("toggle")){
				if (args.length >= 2) {
					if(config.getString("twists."+args[1]) == "true" || config.getBoolean("twists."+args[1]) == true){
						if (args[1].equalsIgnoreCase("more-diamond-upgrades")) {
							moreDiamondUpgrades(false);
						}
						config.set("twists."+args[1], "false");
						sender.sendMessage("Toggled " + args[1] + " to false");
						plugin.saveConfig();
						plugin.reloadConfig();
					}
					else if(config.getString("twists."+args[1]) == "false" || config.getBoolean("twists."+args[1]) == false){
						if (args[1].equalsIgnoreCase("more-diamond-upgrades")) {
							moreDiamondUpgrades(true);
						}
						config.set("twists."+args[1], "true");
						sender.sendMessage("Toggled " + args[1] + " to true");
						plugin.saveConfig();
						plugin.reloadConfig();
					}
				}
			}
		}
		// TODO Auto-generated method stub
		return false;
	}


